"""
Docstring for Classes&Objects.Example1

Class is a blueprint of an object
"""

class Student: # <- Blueprint

    name = "Arijit" 

s1 = Student() # <- Object
print(s1.name)


class Car:

    color = "blue"
    brand = "BMW"

# Creating an object [c1 is the first object]
c1 = Car()
print(c1.color)
print(c1.brand)